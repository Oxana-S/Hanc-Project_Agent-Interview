def create_txt(company_dir: Path, data: dict):
    """Create TXT file."""
    path = company_dir / "company_info.txt"
    with open(path, "w", encoding="utf-8") as f:
        f.write(data["txt_content"])
    print(f"  TXT: {path.name} ({path.stat().st_size} bytes)")


def create_xlsx(company_dir: Path, data: dict):
    """Create XLSX file."""
    from openpyxl import Workbook
    from openpyxl.styles import Font, Alignment, PatternFill

    wb = Workbook()
    first = True

    for sheet_name, rows in data["xlsx_data"].items():
        if first:
            ws = wb.active
            ws.title = sheet_name
            first = False
        else:
            ws = wb.create_sheet(title=sheet_name)

        for row_idx, row in enumerate(rows, 1):
            for col_idx, value in enumerate(row, 1):
                cell = ws.cell(row=row_idx, column=col_idx, value=value)
                if row_idx == 1:  # Header
                    cell.font = Font(bold=True)
                    cell.fill = PatternFill(start_color="4472C4", end_color="4472C4", fill_type="solid")
                    cell.font = Font(bold=True, color="FFFFFF")
                    cell.alignment = Alignment(horizontal="center")

        # Auto-width columns
        for col in ws.columns:
            max_length = 0
            for cell in col:
                if cell.value:
                    max_length = max(max_length, len(str(cell.value)))
            ws.column_dimensions[col[0].column_letter].width = max_length + 4

    path = company_dir / "data.xlsx"
    wb.save(path)
    print(f"  XLSX: {path.name} ({path.stat().st_size} bytes)")


def create_docx(company_dir: Path, data: dict):
    """Create DOCX file."""
    from docx import Document
    from docx.shared import Pt, Inches
    from docx.enum.text import WD_ALIGN_PARAGRAPH

    doc = Document()
    docx_data = data["docx_sections"]

    # Title
    title = doc.add_heading(docx_data["title"], level=0)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # Sections
    for heading, content in docx_data["sections"]:
        doc.add_heading(heading, level=1)
        doc.add_paragraph(content)

    # Table
    if "table" in docx_data:
        doc.add_heading("–ö–ª—é—á–µ–≤—ã–µ –ø–æ–∫–∞–∑–∞—Ç–µ–ª–∏", level=1)
        table_data = docx_data["table"]
        table = doc.add_table(rows=len(table_data), cols=len(table_data[0]))
        table.style = "Table Grid"

        for i, row in enumerate(table_data):
            for j, cell in enumerate(row):
                table.rows[i].cells[j].text = str(cell)
                if i == 0:
                    for paragraph in table.rows[i].cells[j].paragraphs:
                        for run in paragraph.runs:
                            run.bold = True

    path = company_dir / "commercial_offer.docx"
    doc.save(path)
    print(f"  DOCX: {path.name} ({path.stat().st_size} bytes)")


